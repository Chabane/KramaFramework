package org.kramaframework.exception;

import org.jdom.JDOMException;

/**
 * This exception is generated when a krama configuration xml file is not correctly
 * defined. ie : If format of xml file not corresponding with DTD, SAX throw exception 
 * @author chabane
 * @version 1.0
 */
public class KramaConfigurationBuildFailedException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/**
	 * Constructor
	 * @param message
	 * @param exception
	 */
	public KramaConfigurationBuildFailedException(String message, JDOMException exception) {
		super(message, exception);
	}
}
